// check the given alphabet is vowel or consonant

function alphabet(val){


    switch(val){
        case "a":
           return  "vowels" ;
        case  "e":
            return "vowels";
        case  "i" :
            return  "vowels";
        
        case  "o" :
             return "vowels";
        case "u" :
             return  "vowels";



            default :
            return "consonant";     

    }  

}

